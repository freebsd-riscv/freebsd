ENTRY(cpu_exception_handler_sv)
	save_registers
	mv	a0, sp
	call	_C_LABEL(do_trap)
	load_registers
	eret
END(cpu_exception_handler_sv)

ENTRY(cpu_exception_handler_user)
	csrrw   sp, sscratch, sp

	save_registers
	mv	a0, sp
	call	_C_LABEL(do_trap)
	load_registers

	csrrw   sp, sscratch, sp

	eret
END(cpu_exception_handler_user)

