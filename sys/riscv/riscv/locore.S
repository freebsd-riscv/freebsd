ENTRY(user_trap)
	csrrw   sp, mscratch, sp
	addi    sp, sp, -64
	sd      t0, 0(sp)

	csrr    t0, mcause
	bltz    t0, .Luser_interrupt
	
	/* Setup supervisor exception handler */
	la	t0, _C_LABEL(cpu_exception_handler_user)
	li	t1, 0xffffffffc0000000
	add	t0, t0, t1
	csrw	stvec, t0

	/* Restore state */
	ld      t0, 0(sp)
	addi	sp, sp, 64
	csrrw   sp, mscratch, sp

	/* Redirect to supervisor */
	mrts

.Luser_interrupt:
	/* Disable interrupts */
	li	t0, MIE_MTIE
	csrc	mie, t0

	/* Clear pending */
	li	t0, MIP_MTIP
	csrc	mip, t0

	/* Clear supervisor pending */
	li	t0, MIP_STIP
	csrc	mip, t0

	ld      t0, 0(sp)
	addi	sp, sp, 64
	csrrw   sp, mscratch, sp
	eret
END(user_trap)

#define	HANDLE_SUPERVISOR_TRAP_IN_MACHINE_MODE 0 \
  | (0 << (31 - 0)) /* IF misaligned */           \
  | (0 << (31 - 1)) /* IF fault */                \
  | (1 << (31 - 2)) /* illegal instruction */     \
  | (0 << (31 - 3)) /* breakpoint */              \
  | (1 << (31 - 4)) /* load misaligned */         \
  | (0 << (31 - 5)) /* load fault */              \
  | (1 << (31 - 6)) /* store misaligned */        \
  | (0 << (31 - 7)) /* store fault */             \
  | (0 << (31 - 8)) /* user environment call */   \
  | (1 << (31 - 9)) /* super environment call */  \

sv_trap:
	/* Save state */
	csrrw	sp, mscratch, sp
	addi	sp, sp, -64
	sd	t0, 0(sp)
	sd	t1, 8(sp)
	sd	t4, 16(sp)
	sd	t5, 24(sp)
	sd	t6, 32(sp)

	/* Save current sptbr */
	//csrr	t0, sptbr
	//sd	t0, 40(sp)
	//mv	x4, t0
	/* Load kernel sptbr */
	//la	t0, pagetable_l1
	//csrw	sptbr, t0

	/* Debugging */
	csrr	t0, mstatus

	//csrr	t0, sptbr /* debug */
	csrr	t0, mcause
	bltz	t0, .Linterrupt
	li	t1, HANDLE_SUPERVISOR_TRAP_IN_MACHINE_MODE
	sllw	t1, t1, t0
	bltz	t1, .Lhandle_trap_in_machine_mode

	/* Setup supervisor exception handler */
	la	t0, _C_LABEL(cpu_exception_handler_sv)
	li	t1, 0xffffffffc0000000
	add	t0, t0, t1
	csrw	stvec, t0

	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp

	/* Redirect to supervisor */
	mrts

.Linterrupt:
	/* Supervisor interrupt ? */
	//csrr	t0, mstatus
	//and	t0, t0, (MSTATUS_PRV_MASK)
	//li	t1, (MSTATUS_PRV_S << MSTATUS_PRV_SHIFT)
	//csrr	t0, mip
	//and	t0, t0, (MIP_STIP)
	//li	t1, (MIP_STIP)
	//beq	t0, t1, .Lmrts

	/* Debugging */
	csrr	t0, mstatus

	/* Disable interrupts */
	li	t0, MIE_MTIE
	csrc	mie, t0

	/* Clear pending */
	li	t0, MIP_MTIP
	csrc	mip, t0

	/* Post supervisor interrupt */
	li	t0, MIP_STIP
	csrs	mip, t0

	/*
	 * Check if we get here from supervisor mode,
	 * was supervisor interrupts enabled before trap ?
	 */
	csrr	t0, mstatus
	li	t1, (SR_IE1 | (MSTATUS_PRV_S << MSTATUS_PRV1_SHIFT))
	and	t0, t0, t1
	beq	t0, t1, .Lmrts

	/* Restore sptbr */
	//ld	t0, 40(sp)
	//csrw	sptbr, t0
	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp
	eret

.Lmrts:
	//csrr	t0, mip
	//and	t0, t0, MIP_MTIP
	//li	t1, MIP_MTIP
	//beq	t0, t1, .Lpost

	/* Enable interrupts for PRV1, so when we leave this trap it will be PRV */
	//li	t0, (SR_IE1)
	//csrs	mstatus, t0

	/* Clear pending */
	li      t0, MIP_STIP
	csrc    mip, t0

	/* Setup supervisor exception handler */
	la	t0, _C_LABEL(cpu_exception_handler_sv)
	li	t1, 0xffffffffc0000000
	add	t0, t0, t1
	csrw	stvec, t0

	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp

	/* Redirect to supervisor */
	mrts

.Lhandle_trap_in_machine_mode:

	/* Save mepc, point to the next instr */
	csrr	t1, mepc
	addi	t1, t1, 4
	li	t4, ECALL_LOW_PRINTC
	beq	t5, t4, low_printc
	li	t4, ECALL_MTIMECMP
	beq	t5, t4, set_mtimecmp
	li	t4, ECALL_CLEAR_PENDING
	beq	t5, t4, clear_pending

	/* Exit trap */
	csrw    mepc, t1
	/* Restore sptbr */
	//ld	t0, 40(sp)
	//csrw	sptbr, t0
	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp
	eret

low_printc:
	//li	t5, 0
	li	t0, 0x101000000000000
	//add	t0, t0, t6
	add	t0, t0, a0

2:
	csrrw	t0, mtohost, t0
	bnez	t0, 2b

	csrr	t0, mfromhost
	li	t0, 0
	csrw	mfromhost, t0

	/* Exit trap */
	csrw    mepc, t1
	/* Restore sptbr */
	//ld	t0, 40(sp)
	//csrw	sptbr, t0
	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp
	eret

set_mtimecmp:
	csrw	mtimecmp, t6

	/* Enable interrupts */
	li	t0, (MIE_MTIE | MIE_STIE)
	csrs	mie, t0

	/* Exit trap */
	csrw    mepc, t1
	/* Restore sptbr */
	//ld	t0, 40(sp)
	//csrw	sptbr, t0
	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp
	eret

clear_pending:
	li      t0, MIP_STIP
	csrc    mip, t0

	/* Exit trap */
	csrw    mepc, t1
	/* Restore sptbr */
	//ld	t0, 40(sp)
	//csrw	sptbr, t0
	/* Restore state */
	ld	t0, 0(sp)
	ld	t1, 8(sp)
	ld	t4, 16(sp)
	ld	t5, 24(sp)
	ld	t6, 32(sp)
	addi	sp, sp, 64
	csrrw	sp, mscratch, sp
	eret

mm_trap:
